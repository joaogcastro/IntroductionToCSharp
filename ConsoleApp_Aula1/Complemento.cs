public class Complemento
{
    private int _id;
    private string _descricao;
    private int _numero;

    public int Id
    {
        get => _id;
        set => _id = value;
    }

    public string Descricao
    {
        get => _descricao;
        set => _descricao = value;
    }

    public int Numero
    {
        get => _numero;
        set => _numero = value;
    }
}